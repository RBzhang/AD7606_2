// Description: AD7606/AD7606C parallel read controller.
//
//              rst_n (active low) from PS FCLK_RESET0_N resets all state and
//              clears error flags. The ADC itself is reset by the S_RESET state,
//              which drives adc_reset high for RESET_CLKS cycles.
//
//              The sampling time base is generated by an independent fixed-period
//              sample_tick generator. CONVST is launched only on sample_tick.
//              The read FSM must finish conversion/readout within one sample
//              period. If it cannot finish, overrun_o is pulsed.
//
//              Channel data is read sequentially via adc_db_synced (registered
//              input bus for metastability protection), copied into temporary
//              registers, moved to a frame buffer when the frame is complete,
//              and transferred to ch1_data~ch4_data on the next sample_tick.
//              Output data_valid aligns to the strict sample period, with one
//              sample-frame latency.
// 
// Dependencies: sample_tick_gen.v
// 
// Revision:
// Revision 0.01 - File Created
// Revision 0.02 - Add temporary channel registers for frame-synchronous output
// Revision 0.03 - Add strict sample tick timing and one-frame-latency output
// Revision 0.04 - Remove external rst_n and use register initial values
// Additional Comments:
// 
